export function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

export function pickFromList(l) {
  const random = Math.floor(Math.random() * l.length);
  return l[random];
}
export function pickFromRange(min, max) {
  return Math.floor(Math.random() * (max - min) + min);
}

export function pickDigits(length) {
  let digits = "";
  for (let i = 0; i < length; i++) {
    digits += ((Math.random() * 100) | 0) % 10;
  }
  return digits;
}

export function computeCCLugn(digits) {
  let sum = 0;
  for (let i = 0; i < digits.length; i++) {
    let digitValue = parseInt(digits.charAt(i));
    let isMultiplied = i % 2 != digits.length % 2;
    if (isMultiplied) {
      digitValue *= 2;
      if (digitValue > 9) {
        digitValue -= 9;
      }
    }
    // console.log(i, digitValue, isMultiplied, sum, sum + digitValue);
    sum += digitValue;
  }
  // console.log(sum);
  return (sum * 9) % 10;
}
export function pickCCNumber() {
  let digits = "";
  if (Math.random() < 0.4) {
    // Visa
    digits += "4" + pickDigits(1);
  } else {
    // MasterCard
    digits += "5" + pickFromRange(1, 6);
  }
  // 2 = prefix, 1 = verif
  digits += pickDigits(16 - 2 - 1);
  digits += computeCCLugn(digits);
  return digits;
}

export function encodeForm(form) {
  let entries = [];
  for (const [key, value] of Object.entries(form)) {
    entries.push(`${encodeURI(key)}=${encodeURI(value)}`);
  }
  return entries.join("&");
}